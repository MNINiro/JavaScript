var x = 10
var y = 20

// Less than
document.write("Less than:" + (x<y)) // will display output
document.write("<br>")

// Greater than
document.write("Greater than:"+ (x>y)) // will display output
document.write("<br>")

// Equal to
document.write("Equal to:" + (x==y)) // will display output
document.write("<br>")

// Not equal to
document.write("Not Equal to:" + (y!=x)) // will display output
document.write("<br>")

// Greater than or equal to
document.write("Greater than or equal to:" + (x >= y)) // will display output
document.write("<br>")

// Less than or equal to
document.write("Less than or equal to:" + (x <= y)) // will display output
document.write("<br>")

// Equal value and same type
var x = 10
var y = 10
document.write("Equal value and same type:" + (x===y)) // will display output
document.write("<br>")

// Not Equal value or Not same type
var x = 10
var y = "a"
document.write("Not Equal value or Not same type:" + (x!==y)) // will display output

