// +=
var x = 5;
x += 10;       
document.write("+=:" + x) // will display output
document.write("<br>")

// -=
var x = 15;
x -= 10;       
document.write("-=:" + x) // will display output
document.write("<br>")

// *=
var x = 5;
x *= 10;       
document.write("*=:" + x) // will display output
document.write("<br>")

// /=
var x = 25;
x /= 5;       
document.write("/=:" + x) // will display output
document.write("<br>")

// %=
var x = 15;
x %= 6;       
document.write("%=:" + x) // will display output
document.write("<br>")

// <<=
var x = 25;
x <<= 2;       
document.write("<<=:" + x) // will display output
document.write("<br>")

// >>=
var x = 25;
x >>= 2;       
document.write(">>=:" + x) // will display output
document.write("<br>")

// >>>=
var x = 25;
x >>>= 2;       
document.write(">>>=:" + x) // will display output
document.write("<br>")

// &=
var x = 25;
var y = 10
c = x &= y;       
document.write("&=:" + c) // will display output
document.write("<br>")

// ^=
var x = 10;
var y = 5
c = x ^= y;       
document.write("^=:" + c) // will display output
document.write("<br>")

// |=
var x = 10;
var y = 5
c = x |= y;       
document.write("|=:" + c) // will display output
document.write("<br>")