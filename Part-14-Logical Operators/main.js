// AND
var x = 10
var y = 20
document.write("AND:" + ((x<15) && (y>15))) // will display output
document.write("<br>")

// OR
var x = 10
var y = 20
document.write("OR:" + ((x<15) || (y>=20))) // will display output
document.write("<br>")

// NOT
var x = 10
var y = 20
document.write("NOT:" + !(x<y)) // will display output
document.write("<br>")


